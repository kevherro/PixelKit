/// PixelKit is a collection of custom SwiftUI components and styles
/// designed to enhance the visual appeal and functionality of your app's UI.
public struct PixelKit {
  /// Initializes a new instance of PixelKit.
  ///
  /// This initializer doesn't take any parameters as PixelKit
  /// is primarily a namespace for accessing various UI components.
  public init() {}
}

extension PixelKit {
  /// Namespace for animation-related components and modifiers.
  ///
  /// This struct contains various animated UI elements and
  /// SwiftUI view modifiers that can be used to add dynamic
  /// visual effects to your app's interface.
  public struct Animations {}

  /// Namespace for indicator components.
  ///
  /// This struct contains various indicators that can
  /// be customized and used directly.
  public struct Indicators {}

  /// Namespace for custom styles applicable to SwiftUI views.
  ///
  /// This struct contains different style definitions that can be
  /// applied to standard SwiftUI components to customize their
  /// appearance and behavior.
  public struct Styles {}
}
